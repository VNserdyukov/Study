package OOP.Module1.Book;

public class Book {
    private String name;
    private String author;
    private String publisher;
    private int age;

    public Book() {
    }

    public Book(String name, String author, String publisher, int age) {
        this.name = name;
        this.author = author;
        this.publisher = publisher;
        this.age = age;
    }

}
